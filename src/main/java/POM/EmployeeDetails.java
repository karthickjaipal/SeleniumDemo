package POM;

public class EmployeeDetails {
	
	String empId;
	String empName;
	String empAge;
	String empMobileNumber;
	String empLocality;
	
	public String getEmpId() {
		return empId;
	}
	public void setEmpId(String empId) {
		this.empId = empId;
	}
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	public String getEmpAge() {
		return empAge;
	}
	public void setEmpAge(String empAge) {
		this.empAge = empAge;
	}
	public String getEmpMobileNumber() {
		return empMobileNumber;
	}
	public void setEmpMobileNumber(String empMobileNumber) {
		this.empMobileNumber = empMobileNumber;
	}
	public String getEmpLocality() {
		return empLocality;
	}
	public void setEmpLocality(String empLocality) {
		this.empLocality = empLocality;
	}
	
	

}
